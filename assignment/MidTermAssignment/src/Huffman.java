import java.util.Comparator;
import java.util.PriorityQueue;

// Huffman Code Class
public class Huffman {

    private final int totalLen;
    private final String[] words;
    private final int[] freqArr;

    // Parameter Constructor
    public Huffman(String[] words, int[] freqArr) {
        this.words = words;
        this.freqArr = freqArr;
        this.totalLen = words.length;
    }

    public void huffmanCode(){
        PriorityQueue<Node> priorityQueue = new PriorityQueue<>(totalLen, new CompareNode());
        for (int i = 0; i < totalLen; i++) {
            Node nd = new Node();
            nd.code = words[i];
            nd.item = freqArr[i];
            nd.left = null;
            nd.right = null;
            priorityQueue.add(nd);
        }

        // Create Root Node
        Node root = null;

        while(priorityQueue.size() > 1){
            //first min extract.
            Node first = priorityQueue.peek();
            priorityQueue.poll();

            //second min extract.
            Node second = priorityQueue.peek();
            priorityQueue.poll();

            //new node  which is equal
            Node f = new Node();


            f.item = first.item + second.item;
            f.code = "-";

            // first extracted node as left child.
            f.left = first;

            // second extracted node as the right child.
            f.right = second;

            // marking the f node as the root node.
            root = f;

            // add this node to the priority-queue.
            priorityQueue.add(f);
        }
        printCode(root, "");
    }

    private void printCode(Node node, String str){
        if(node.left == null && node.right == null && !(node.code.isBlank())){
            System.out.println(node.code + "      :    " + str);
            return;
        }
        printCode(node.left, str + "0");
        printCode(node.right, str + "1");
    }
}

// For Comparing the Nodes
class CompareNode implements Comparator<Node> {
    @Override
    public int compare(Node o1, Node o2) {
        return o1.item - o2.item;
    }
}


